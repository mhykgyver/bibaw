import 'package:bibaw_app/features/app/domain/entities/infant.dart';
import 'package:flutter/widgets.dart';

class InfantModel extends Infant {
  const InfantModel({
    @required infantID,
    @required firstName,
    @required lastName,
    middleInitial,
    @required gender,
    @required birthDate,
    birthPlace,
    @required weight,
    @required height,
    @required circumferenceHead,
    circumferenceChest,
    circumferenceAbdominal,
    scoreAPGAR,
    birthProblems,
    distinguishingMarks,
    deliveryType,
    @required parentID,
    obstetricianID,
    pediatricianID,
    hospitalID,
  }) : super(
          infantID: infantID,
          firstName: firstName,
          lastName: lastName,
          middleInitial: middleInitial,
          gender: gender,
          birthDate: birthDate,
          birthPlace: birthPlace,
          weight: weight,
          height: height,
          circumferenceHead: circumferenceHead,
          circumferenceChest: circumferenceChest,
          circumferenceAbdominal: circumferenceAbdominal,
          scoreAPGAR: scoreAPGAR,
          birthProblems: birthProblems,
          distinguishingMarks: distinguishingMarks,
          deliveryType: deliveryType,
          parentID: parentID,
          obstetricianID: obstetricianID,
          pediatricianID: pediatricianID,
          hospitalID: hospitalID,
        );

  factory InfantModel.fromJson(Map<String, dynamic> json) {
    return InfantModel(
      infantID: json['infantID'],
      firstName: json['firstName'],
      lastName: json['lastName'],
      middleInitial: json['middleInitial'],
      gender: json['gender'],
      birthDate: DateTime.parse(json['birthDate']),
      birthPlace: json['birthPlace'],
      weight: json['weight'],
      height: json['height'],
      circumferenceHead: json['circumferenceHead'],
      circumferenceChest: json['circumferenceChest'],
      circumferenceAbdominal: json['circumferenceAbdominal'],
      scoreAPGAR: json['scoreAPGAR'],
      birthProblems: json['birthProblems'],
      distinguishingMarks: json['distinguishingMarks'],
      deliveryType: json['deliveryType'],
      parentID: json['parentID'],
      obstetricianID: json['obstetricianID'],
      pediatricianID: json['pediatricianID'],
      hospitalID: json['hospitalID'],
    );
  }

  Map<String, dynamic> toJson() {
    return {
      'infantID': infantID,
      'firstName': firstName,
      'lastName': lastName,
      'middleInitial': middleInitial,
      'gender': gender,
      'birthDate': birthDate,
      'birthPlace': birthPlace,
      'weight': weight,
      'height': height,
      'circumferenceHead': circumferenceHead,
      'circumferenceChest': circumferenceChest,
      'circumferenceAbdominal': circumferenceAbdominal,
      'scoreAPGAR': scoreAPGAR,
      'birthProblems': birthProblems,
      'distinguishingMarks': distinguishingMarks,
      'deliveryType': deliveryType,
      'parentID': parentID,
      'obstetricianID': obstetricianID,
      'pediatricianID': pediatricianID,
      'hospitalID': hospitalID,
    };
  }
}
