    obstetricianID,
    pediatricianID,
    hospitalID,
  }) : super(
            infantID: infantID,
            firstName: firstName,
            lastName: lastName,
            middleInitial: middleInitial,
            gender: gender,
            birthDate: birthDate,
            birthPlace: birthPlace,
            weight: weight,
            height: height,
            circumferenceHead: circumferenceHead,
            circumferenceChest: circumferenceChest,
            circumferenceAbdominal: circumferenceAbdominal,
